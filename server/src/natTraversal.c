int ntMsgQCreate()
{
	int rv;
	rv = ntMsgQFileCreate(NT_FILE_PATH);
	ERR_CHECK_GOTO(rv, ERR);
	key_t msqid = ftok(NT_FILE_PATH,NT_SUB_SER_NUM);
	rv = msgget(msqid, 0666|IPC_CREAT);
	if(ERROR == rv)
	{
		DEBUG_ERROR("msgQ create fail");
		rv = ERR_INIT_FAIL;
		goto ERR;
	}
	

	return ERR_NO_ERROR;
ERR:
	return rv;
}


